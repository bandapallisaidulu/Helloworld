package Firsthand_Java;

public class Ifelse {
	public static void main (String args[]) {
		int num1 = 200;
		int num2 = 1000;
		int num3 = 2000;
		
		if(num1 > num2) {
			System.out.print(num1);
			
		
			
		}
		else if (num2 > num3){ 
			System.out.print(num2);
			
			
		}
		else {
			System.out.print(num3);
		}

		
	}

}
